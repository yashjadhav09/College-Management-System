    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        try {
            String sql = "select  * from  stud where Roll=?";
            pst = conn.prepareStatement(sql);
            pst.setString(1, txt.getText());

            rs = pst.executeQuery();

            if (rs.next()) {
                String add1 = rs.getString("Roll");
                roll.setText(add1);
                String add2 = rs.getString("Name");
                name.setText(add2);
                String add3 = rs.getString("Course");
                Course.setText(add3);
                String add4 = rs.getString("Year");
                Year.setText(add4);
                String add5 = rs.getString("Sem");
                sem.setText(add5);
                String add6 = rs.getString("cast");
                cast.setText(add6);
                String add7 = rs.getString("fee");
                fee.setText(add7);
                String add8 = rs.getString("paid");
                fee1.setText(add8);

            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "No Record Found");

        }


    }//GEN-LAST:event_jButton2ActionPerformed

