    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String acceptedamount = "";
        try {
            String sql = "select  * from  fee where Roll=?";
            pst = conn.prepareStatement(sql);
            pst.setString(1, txt.getText());

            rs = pst.executeQuery();

            if (rs.next()) {
                acceptedamount = rs.getString("paid");

            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "No Record Found");

        }
        fee1.setText(acceptedamount);
        int f = Integer.parseInt(fee.getText());
        paid.setText("" + f);
        float a = Integer.parseInt(fee.getText());
        float b = Integer.parseInt(fee1.getText());

        if (a == b) {
            paid.setText("Amount Paid");
            ss.setText("Fee already paid,no need to pay amount !");
        }

        // TODO add your handling code here:
    }//GEN-LAST:event_jButton1ActionPerformed

