    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        int p = JOptionPane.showConfirmDialog(null, "Are you Sure to Subimt?", "Save", JOptionPane.YES_NO_OPTION);
        if (p == 0) {
            try {

                String sql = "INSERT INTO fee (Roll,Name,course,Year,Semister,cast,fee,paid) values(?,?,?,?,?,?,?,?)";

                pst = conn.prepareStatement(sql);

                pst.setString(1, txt.getText());
                pst.setString(2, name.getText());
                pst.setString(3, Course.getText());
                pst.setString(4, Year.getText());
                pst.setString(5, sem.getText());
                pst.setString(6, cast.getText());
                pst.setString(7, fee.getText());
                pst.setString(8, paid.getText());

                pst.execute();

                JOptionPane.showMessageDialog(null, "Amount submited Successfully.");

            } catch (Exception e) {

                JOptionPane.showMessageDialog(null, e);
            }
        }
    }//GEN-LAST:event_jButton3ActionPerformed

