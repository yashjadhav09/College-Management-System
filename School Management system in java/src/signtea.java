    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String username=user.getText();
        String password=pass.getText();
        String passwordc=passc.getText();

        if(username.equals("")){
            show1.setText("Please Enter Username !");
        }
        else if(password.equals("")){
            show1.setText("Please Enter Password !");
        }
        else if( passwordc.equals("")){
            show1.setText("Please Enter Comfirm Password !");
        }else if(!password.equals(passwordc))
        {
            show1.setText("Please Comfirm Password !");
        }
        else{
            try {

                String sql="insert into techpass (username,password) values(?,?)";

                pst=conn.prepareStatement(sql);

                pst.setString(1, user.getText());
                pst.setString(2, pass.getText());

                pst.execute();
                JOptionPane.showMessageDialog(null, "Teacher Sign Up Successfully.");

            } catch (Exception e) {

                JOptionPane.showMessageDialog(null, e);
            }
        }

    }//GEN-LAST:event_jButton2ActionPerformed

