    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        int p=JOptionPane.showConfirmDialog(null, "Are you Sure to Save?","Save",JOptionPane.YES_NO_OPTION);
        if(p==0)
        {
            try {

                String sql="insert into stud (Roll,Name,Gender,Course,Year,Sem,Dept,Email,Father,cast,fee,StudContact,DOB,City,State,Address) values(?,?,?,?,?,?,?,?,?,?,?,?,?,?,?,?)";

                pst=conn.prepareStatement(sql);

                pst.setString(1, roll.getText());
                pst.setString(2, fname.getText());
                pst.setString(3, gender.getText());
                
                String value1=jComboBox1.getSelectedItem().toString();
                pst.setString(4, value1);
                pst.setString(5, Year.getText());
                pst.setString(6, sem.getText());
                
                String value=jComboBox1.getSelectedItem().toString();
                pst.setString(7, value);
                pst.setString(8, mail.getText());
                pst.setString(9, father.getText());
                 String value2=jcm.getSelectedItem().toString();
                pst.setString(10, value2);
                pst.setString(11, pc.getText());
                pst.setString(12, sc.getText());
                pst.setString(13, dob.getText());
                pst.setString(14, ct.getText());
                pst.setString(15, state.getText());
                pst.setString(16, ad.getText());

                pst.execute();
                JOptionPane.showMessageDialog(null, "Data Save Successfully.");

            } catch (Exception e) {

                JOptionPane.showMessageDialog(null, e);
            }
        }

    }//GEN-LAST:event_jButton3ActionPerformed

